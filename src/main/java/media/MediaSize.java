package media;

public class MediaSize {
	
	SizeMedium medium;
	SizeThumb thumb;
	SizeSmall small;
	SizeLarge large;
	
	public SizeMedium getMedium() {
		return medium;
	}
	public void setMedium(SizeMedium medium) {
		this.medium = medium;
	}
	public SizeThumb getThumb() {
		return thumb;
	}
	public void setThumb(SizeThumb thumb) {
		this.thumb = thumb;
	}
	public SizeSmall getSmall() {
		return small;
	}
	public void setSmall(SizeSmall small) {
		this.small = small;
	}
	public SizeLarge getLarge() {
		return large;
	}
	public void setLarge(SizeLarge large) {
		this.large = large;
	}
	
}
